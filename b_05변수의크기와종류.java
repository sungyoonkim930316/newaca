package day01;

public class b_05변수의크기와종류 {

	public static void main(String[] args) {

		
		// 정수 int(4byte) long(8byte)
		
		// int a=30000000000; 숫자가 int값의 최대범위를 넘어가서 int에 넣을 수 없다.
		long a=30000000000L; // int와 구분을 위해 뒤에 대문자L을 붙인다.
		
		// 실수 float(4byte) double(8byte)
		
		//float c=1.123412341234; 숫자가 float값의 최대범위를 넘어가서 float에 넣을 수 없다.
		double c=1.123412341234;
		
	}

}
