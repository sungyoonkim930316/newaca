		System.out.printf("4번 문제 정답 = ");
		System.out.printf("%.2fkg\n", 11 / 100.0 * 3);
		// 문제 끝난 시간 13: 26

		// ------------------------------------------------------------------------------------------------
		// 예시 문제 시작 시간 14: 24
