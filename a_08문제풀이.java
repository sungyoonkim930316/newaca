		System.out.printf("%.0f개\n", 345 / (1 - 0.16 - 0.15));

		// 문제 끝난 시간 14: 27

		// ------------------------------------------------------------------------------------------------
		// 예시 문제 시작 시간 14: 28
		/*
		 * [문제] - 사과 8개의 무게는 1056g 입니다. - 배는 사과의 117% 의 무게를 갖고있습니다. - 사과 5개 배7개를
		 * 구입하였습니다. - 과일전체의 총무게를 구하시오.
		 */
		System.out.printf("6번 문제 정답 = ");
		System.out.printf("%.2fg\n", ((1056 / 8) * 5) + ((1056 / 8) * 1.17 * 7));

		// 문제 끝난 시간 14: 32
		
		

